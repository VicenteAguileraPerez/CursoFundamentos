package fundamentos._07_do_while;

public class DoWhile1
{
    public static void main(String[] args) {
        int i;

        //inicializaci√≥n
        i=1;
        do
        {

            //las sentencias a realizar
            System.out.println(i);

            //incremento o decremento
            i++;
        }while(i<=10);// condicion
    }
}
